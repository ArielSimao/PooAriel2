    private void preencherTabela() {
        DefaultTableModel model = (DefaultTableModel) jTable1.getModel();
        model.setNumRows(0);
        for (Produto item : listaProduto) {
            model.addRow(new Object[]{
                item.getIdProduto(),
                item.getDescricao(),
                item.getQtdDisponivel(),
                item.getPreco()
            });
        }
    }
