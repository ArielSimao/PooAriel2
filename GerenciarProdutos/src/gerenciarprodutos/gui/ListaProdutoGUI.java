    private void btnEntradaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEntradaActionPerformed
        // TODO add your handling code here:
        Produto p = null;
        int codigoProc = Integer.parseInt(txtCodigo.getText());
        for (Produto item : listaProduto) {
            if(item.getIdProduto()==codigoProc){
                p = item;
                String mens = p.entrada(Integer.parseInt(txtQuantidade.getText()));
                JOptionPane.showMessageDialog(null, mens);
                jTable1.removeAll();
                this.preencherTabela();
                return;
            }
        }
        JOptionPane.showMessageDialog(null, "Produto não localizado");
    }//GEN-LAST:event_btnEntradaActionPerformed

